package extend_lesson.buoi7.shape;

public class Square1 extends Shape1{
    private double side;

    public Square1(double side) {
        this.side = side;
    }

    public double getSide() {
        return side;
    }

    public void setSide(double side) {
        this.side = side;
    }

    @Override
    public String toString() {
        return "Square{" +
                "side=" + side +
                '}';
    }
}
